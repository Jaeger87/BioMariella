package apimodel;

public class UserProfile {

	private String username;
	private int score;
	
	public String getUsername() {
		return username;
	}
	public int getScore() {
		return score;
	}
	
	public void setUsername(String username) {
		this.username = username;
	}
	
	public void setScore(int score) {
		this.score = score;
	}
	
	
}
